public class Barbero {
boolean duerme = true;
boolean corta = false;
boolean cobra = true;

    public boolean isDuerme() {
        return duerme;
    }

    public void setDuerme(boolean duerme) {
        this.duerme = duerme;
    }

    public boolean isCorta() {
        return corta;
    }

    public void setCorta(boolean corta) {
        this.corta = corta;
    }

    public boolean isCobra() {
        return cobra;
    }

    public void setCobra(boolean cobra) {
        this.cobra = cobra;
    }
}
